package com.kodilla.stream.reference;

public class FuncionalCalculator {
    public static double multiplyBy(double a, double b) {
        return a * b;
    }

    public static double addAToB(double a, double b){
        return a + b;
    }

    public static double subFromA(double a, double b){
        return a - b;
    }

    public static double divideAByB(double a, double b){
        return a / b;
    }
}
